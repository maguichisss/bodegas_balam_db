-- Registro detallado de ventas y comisiones
CREATE TABLE ventas (
    id SERIAL PRIMARY KEY,
    id_inventario INTEGER REFERENCES inventario(id),
    cantidad_vendida INTEGER NOT NULL,
    precio_venta NUMERIC(10, 2) NOT NULL,
    total_venta NUMERIC(10, 2) NOT NULL,
    fecha DATE NOT NULL,
    id_vendedor INTEGER REFERENCES vendedores(id),
    metodo_pago VARCHAR(50), -- EFECTIVO, TRANSFERENCIA
    comision_vendedor NUMERIC(10, 2), -- Diferencia vs precio mínimo
    costo_unitario_lote NUMERIC(10, 2), -- Para cálculo de ganancia neta
    created_at TIMESTAMP DEFAULT NOW(),
    updated_at TIMESTAMP DEFAULT NOW()
);

